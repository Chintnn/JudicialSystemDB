-- 2. Court_Personnel_UNF
CREATE TABLE Court_Personnel_UNF (
    Court_ID INT,
    Person_ID INT,
    Court_Name VARCHAR(100),
    Court_Location VARCHAR(255),
    Jurisdiction_Level VARCHAR(50),
    Judge_Names VARCHAR(255),
    Judge_Designations VARCHAR(255),
    Judge_Experience_Years VARCHAR(255),
    Court_Staff_Names VARCHAR(255),
    Court_Staff_Roles VARCHAR(255),
    Court_Staff_Contacts VARCHAR(255),
    Court_Room_Numbers VARCHAR(255),
    Court_Room_Capacities VARCHAR(255),
    Court_Room_Availability_Statuses VARCHAR(255),
    Person_Names VARCHAR(255),
    Person_DOBs VARCHAR(255),
    Person_Contacts VARCHAR(255),
    Person_Addresses VARCHAR(255),
    Person_National_IDs VARCHAR(255),
    Lawyer_Names VARCHAR(255),
    Lawyer_Contacts VARCHAR(255),
    Lawyer_Specializations VARCHAR(255),
    Lawyer_Bar_Numbers VARCHAR(255),
    Lawyer_Experience_Years VARCHAR(255),
    Police_Station_Names VARCHAR(255),
    Police_Station_Locations VARCHAR(255),
    Police_Station_Codes VARCHAR(255),
    Police_Station_Contacts VARCHAR(255),
    Police_Officer_Badge_Numbers VARCHAR(255),
    Police_Officer_Ranks VARCHAR(255),
    Police_Officer_Departments VARCHAR(255),
    Petitioner_Filing_Dates VARCHAR(255),
    Petitioner_Legal_Rep_Names VARCHAR(255),
    Respondent_Response_Dates VARCHAR(255),
    Respondent_Legal_Rep_Names VARCHAR(255),
    Witness_Testimony_Dates VARCHAR(255),
    Witness_Types VARCHAR(255),
    PRIMARY KEY (Court_ID, Person_ID)
