    Verdict_Penalties VARCHAR(255),
    Hearing_Dates VARCHAR(255),
    Hearing_Times VARCHAR(255),
    Hearing_Descriptions TEXT,
    Hearing_Outcomes VARCHAR(255),
    FIR_Dates VARCHAR(255),
    FIR_Details TEXT,
    FIR_Crime_Locations VARCHAR(255),
    Evidence_Types VARCHAR(255),
    Evidence_Descriptions TEXT,
    Evidence_Submission_Dates VARCHAR(255),
    Charge_Descriptions TEXT,
    Charge_Severity_Levels VARCHAR(255),
    Charge_Dates VARCHAR(255),
    Bail_Granted_Dates VARCHAR(255),
    Bail_Conditions TEXT,
    Bail_Amounts VARCHAR(255),
    Bail_Statuses VARCHAR(255),
    Settlement_Terms TEXT,
    Settlement_Dates VARCHAR(255),
    Settlement_Agreements_Signed VARCHAR(255),
    Case_History_Dates VARCHAR(255),
    Case_History_Status_Updates TEXT,
    Case_History_Notes TEXT,
    Appeal_Dates VARCHAR(255),
    Appeal_Reasons TEXT,
    Appeal_Statuses VARCHAR(255),
    Appeal_Levels VARCHAR(255)
